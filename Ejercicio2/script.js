// Crear una función que me permita sumar dos números

function sumar(a,b) {
	// Suma de los dos números
	const resultado = a+b;
	// Retorne el resultado de la operación
	return resultado;
}

// Llamando a la función con los argumentos 5 para a y 3 para b
const resultadoSuma = sumar(5,3);
// Imprimiremos el resultado que se obtuvo
console.log(resultadoSuma);